def _build_html(company_name: str, insights: dict) -> str:
    sections_parts = []
    for label, key, colour in SECTION_CONFIG:
        items = insights.get(key, [])
        body = (
            "<ul>" + "".join(f"<li>{item}</li>" for item in items) + "</ul>"
            if items else '<p class="no-data">No data found for this category.</p>'
        )
        sections_parts.append(
            f'<div class="card" style="border-left:4px solid {colour}">'
            f'<h2 style="color:{colour}">{label}</h2>{body}</div>'
        )
    return HTML_TEMPLATE.format(
        company_name=company_name,
        generated_at=datetime.now().strftime("%B %d, %Y"),
        sections_html="\n".join(sections_parts),
    )


# ── Resend delivery (primary) ────────────────────────────────────────────────
def _send_via_resend(to_email: str, company_name: str, insights: dict, pdf_bytes: bytes) -> bool:
    try:
        resend: Any = importlib.import_module("resend")  # pip install resend
        resend.api_key = settings.RESEND_API_KEY

        safe_name = company_name.replace(" ", "_").lower()
        pdf_b64 = base64.b64encode(pdf_bytes).decode()

        params = {
            "from": settings.RESEND_FROM,
            "to": [to_email],
            "subject": f"Your {company_name} Insights Report is Ready",
            "html": _build_html(company_name, insights),
            "attachments": [
                {
                    "filename": f"{safe_name}_insights_report.pdf",
                    "content": pdf_b64,
                }
            ],
        }

        result = resend.Emails.send(params)
        logger.info(f"[EmailService] Resend delivery → {to_email}  id={result.get('id')}")
        return True

    except Exception as exc:
        logger.error(f"[EmailService] Resend failed: {exc}")
        return False


# ── SMTP fallback ────────────────────────────────────────────────────────────
def _send_via_smtp(to_email: str, company_name: str, insights: dict, pdf_bytes: bytes) -> bool:
    if not settings.SMTP_ENABLED:
        logger.warning("[EmailService] SMTP_ENABLED=False — skipping SMTP fallback.")
        return False
    if not settings.SMTP_USER or not settings.SMTP_PASS:
        logger.error("[EmailService] SMTP credentials not configured.")
        return False

    msg = MIMEMultipart("mixed")
    msg["Subject"] = f"Your {company_name} Insights Report is Ready"
    msg["From"]    = settings.SMTP_FROM or settings.SMTP_USER
    msg["To"]      = to_email
    msg.attach(MIMEText(_build_html(company_name, insights), "html", "utf-8"))

    pdf_part = MIMEBase("application", "pdf")
    pdf_part.set_payload(pdf_bytes)
    encoders.encode_base64(pdf_part)
    safe_name = company_name.replace(" ", "_").lower()
    pdf_part.add_header("Content-Disposition", "attachment",
                        filename=f"{safe_name}_insights_report.pdf")
    msg.attach(pdf_part)

    try:
        with smtplib.SMTP(settings.SMTP_HOST, settings.SMTP_PORT, timeout=20) as smtp:
            smtp.ehlo()
            smtp.starttls()
            smtp.login(settings.SMTP_USER, settings.SMTP_PASS)
            smtp.sendmail(settings.SMTP_USER, to_email, msg.as_string())
        logger.info(f"[EmailService] SMTP delivery → {to_email}")
        return True
    except Exception as exc:
        logger.error(f"[EmailService] SMTP failed: {exc}")
        return False


# ── Public interface ─────────────────────────────────────────────────────────
class EmailService:
    def send_insights_report(self, to_email: str, insights: dict, pdf_bytes: bytes) -> bool:
        """
        Deliver a PDF report by email.
        Uses Resend if RESEND_API_KEY is set, otherwise falls back to SMTP.
        """
        company_name = insights.get("companyName", "Company")

        if settings.RESEND_API_KEY:
            return _send_via_resend(to_email, company_name, insights, pdf_bytes)
        else:
            logger.warning("[EmailService] RESEND_API_KEY not set — trying SMTP fallback.")
            return _send_via_smtp(to_email, company_name, insights, pdf_bytes)


