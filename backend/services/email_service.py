"""
Sends Company Insights PDF reports via email.

Primary:  Resend API  (https://resend.com) — fast, reliable, zero SMTP config.
Fallback: SMTP        (Gmail / any provider)  — SMTP_ENABLED=True in .env.

Configure in .env:
    RESEND_API_KEY=re_xxxxxxxxxxxx
    RESEND_FROM=onboarding@resend.dev   # free tier; use your domain once verified

    # Only needed if RESEND_API_KEY is empty:
    SMTP_ENABLED=True
    SMTP_HOST=smtp.gmail.com
    SMTP_PORT=587
    SMTP_USER=you@gmail.com
    SMTP_PASS=xxxx xxxx xxxx xxxx
"""

import base64
import smtplib
import logging
import importlib
from typing import Any
from email.mime.multipart import MIMEMultipart
from email.mime.base import MIMEBase
from email.mime.text import MIMEText
from email import encoders
from datetime import datetime

from core.config import settings

logger = logging.getLogger(__name__)


